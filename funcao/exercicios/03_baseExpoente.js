function potencia (base, expoente){
    return Math.pow(base, expoente)
}

[base, expoente] = [2,5]
console.log(`Base: ${base}\texpoente: ${expoente}\tResultado: ${potencia(base,expoente)}`)

base = 6; expoente = 2
console.log(`Base: ${base}\texpoente: ${expoente}\tResultado: ${potencia(base,expoente)}`)

